package com.Neuedu.Entity;

import java.awt.Graphics;
import java.awt.Rectangle;
import java.awt.event.KeyEvent;
import java.util.List;

import com.Neuedu.Action.ActionAble;
import com.Neuedu.Client.GameClient;
import com.Neuedu.Constant.Constants;
import com.Neuedu.Util.GetImageUtil;

/**

* @ClassName: FristPlane

* @Description: 创建角色飞机

* @author QiChen

* @date 2019年8月20日 上午10:26:33

*


*/
public class OurPlane extends GameObj implements ActionAble{
	//速度
	private int speed;	
	//方向boolean变量
	private boolean left,up,right,down;
	//拿客户端
	public GameClient gc;
	//判断我方还是敌方
	public boolean isGood;
	//添加飞机子弹等级变量
	public int BulletLevel = 1;
	//添加血量	值
	public int blood;
	public OurPlane() {
		
	}
	public OurPlane(int x,int y,String imgName,GameClient gc,boolean isGood) {
		this.x = x;
		this.y = y;
		this.img = GetImageUtil.getImg(imgName);
		this.speed = 12;
		this.gc = gc;
		this.isGood = isGood;
		this.blood = 100;
	}
	//移动的方法
	@Override
	public void move() {
		if(left) {
			x -=speed;
		}if(right){
			x +=speed;
		}if(up) {
			y -=speed;
		}if(down) {
			y +=speed;
		}
		outOfBound();
	}
	//画一个我方飞机
	@Override
	public void draw(Graphics g) {
		g.drawImage(img, x, y, null);
		move();
		g.drawString("当前血量值："+blood, 10, 170);
	}
	public void outOfBound() {
		if(y<=30) {
			y = 30;
		}if(x<=5) {
			x = 5;
		}if(x>Constants.GAME_WIDTH-img.getWidth(null)) {
			x = Constants.GAME_WIDTH-img.getWidth(null);
		}if(y>Constants.GAME_HEIGHT-img.getHeight(null)) {
			y = Constants.GAME_HEIGHT-img.getHeight(null);
		}
	}
	//键盘按下
	public void keyPressed(KeyEvent e) {
        switch (e.getKeyCode()) {
        case KeyEvent.VK_A:
        	left = true;
        	break;
        case KeyEvent.VK_D:
        	right = true;
        	break;
        case KeyEvent.VK_W:
        	up = true;
        	break;
        case KeyEvent.VK_S:
        	down = true;
        	break;
        default:
        	break;
        }
	}
	//键盘释放
	public void keyReleased(KeyEvent e) {
		 switch (e.getKeyCode()) {
		 case KeyEvent.VK_A:
			 left = false;
			 break;
		 case KeyEvent.VK_D:
			 right = false;
			 break;
		 case KeyEvent.VK_W:
			 up = false;
			 break;
		 case KeyEvent.VK_S:
			 down = false;
			 break;
	        case KeyEvent.VK_J:
	        	fire();
	        default:
	     	break;
		 }
	}
	//我方飞机开火
	public void fire() {
		//创建我方子弹
		Bullet b = new Bullet(x+this.img.getWidth(null)/2-63,y+this.img.getHeight(null)/2-120,
				"com/Neuedu/Img/Bullet/OurBullet"+BulletLevel+".png",gc,true);
		gc.bullets.add(b);
	}
	//获取当前的形状
		public Rectangle getRec() {
			return new Rectangle(x,y,this.img.getWidth(null),this.img.getHeight(null));
		}
	//检测我方飞机触碰道具	
	public void containItem(Prop prop) {
		if(this.getRec().intersects(prop.getRect())) {
			//移除道具
			gc.props.remove(prop);
			if(BulletLevel>2) {
				BulletLevel = 3;
			}
			//更改子弹等级
			this.BulletLevel +=1;
			
		}
	}
	//检测我方飞机碰撞到多个道具
	public void containItems(List<Prop> props) {
		if(props==null) {
			return;
		}else {
			for(int i=0;i<props.size();i++) {
				containItem(props.get(i));
			}
		}
	}
}
